// APIs provide a structured way for one application to access the capabilities
// of another application. Typically, this communication happens over the internet by
//  way of an API server. A client application (like a mobile app) sends a request
//   to the server, and after the request is processed the server returns a response
//    to the client.

// A request includes the URL of the API endpoint as well as an HTTP request method.
//  The method indicates the type of action you are asking the API to perform. 
//  Here are some of the most common methods:

// GET retrieves data from an API.
// POST sends new data to an API.
// PATCH and PUT update existing data.
// DELETE removes existing data.